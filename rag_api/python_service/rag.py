#ChromaDB is a purpose-built vector database. It's designed specifically to store embeddings and search them efficiently

def get_chroma_client(persist_dir="chroma_store"):
    return chromadb.PersistentClient(path=persist_dir)


def build_chroma_collection(doc_id, chunks, embeddings, persist_dir="chroma_store"):
    client = get_chroma_client(persist_dir)

    # delete existing collection for this doc if re-ingesting
    try:
        client.delete_collection(name=doc_id)
    except:
        pass
    
    #Collection = table for ONE document. Each document gets its own collection. Collections have metadata and support similarity search.
    collection = client.create_collection(
        name=doc_id,
        metadata={"hnsw:space": "cosine"}  # use cosine similarity
    )

    # ChromaDB expects lists, not numpy arrays
    ids = [str(i) for i in range(len(chunks))]
    docs = [c["text"] for c in chunks]
    embeds = [e if isinstance(e, list) else e.tolist() for e in embeddings]
    metadatas = []

    for i, c in enumerate(chunks):
        metadata = {
            "page_num": int(c["page_num"]),
            "type": c["type"],
            "chunk_id": int(i)
        }
        metadatas.append(metadata)

    collection.add(
        ids=ids,
        embeddings=embeds,
        documents=docs,
        metadatas=metadatas
    )

    print(f"ChromaDB collection '{doc_id}' built with {len(chunks)} chunks")
    return collection


def search_chroma(query, doc_id, top_k=10, persist_dir="chroma_store"):
    print(f"\nChromaDB Query: {query}\n")
    client = get_chroma_client(persist_dir)
    collection = client.get_collection(name=doc_id)

    # embed the query
    query_embedding = create_embedding([query])[0]

    results = collection.query(
        query_embeddings=[query_embedding],
        n_results=top_k,
        include=["documents", "metadatas", "distances"]
    )

    # convert to DataFrame to keep rest of pipeline unchanged
    docs = results["documents"][0]
    metas = results["metadatas"][0]
    distances = results["distances"][0]

    df = pd.DataFrame({
        "text": docs,
        "page_num": [m["page_num"] for m in metas],
        "type": [m["type"] for m in metas],
        # chroma cosine distance: 0=identical, 2=opposite → convert to similarity score
        "score": [1 - d for d in distances]
    })

    return df.sort_values(by="score", ascending=False)

def delete_chroma_collection(doc_id, persist_dir="chroma_store"):
    client = get_chroma_client(persist_dir)
    client.delete_collection(name=doc_id)
    print(f"ChromaDB collection '{doc_id}' deleted")
    
def list_chroma_collections(persist_dir="chroma_store"):
    client = get_chroma_client(persist_dir)
    return [c.name for c in client.list_collections()]

def save_suggestions_with_embeddings(doc_id, suggestions, persist_dir="chroma_store"):
    client = get_chroma_client()
    collection = client.get_collection(name=doc_id)
    
    # embed each question locally using ollama
    question_embeddings = []
    for q in suggestions:
        emb = create_embedding([q])[0]
        question_embeddings.append(emb)
    
    # store as JSON: list of {question, embedding}
    data = [
        {"question": q, "embedding": e}
        for q, e in zip(suggestions, question_embeddings)
    ]
    
    collection.upsert(
        ids=["__suggestions__"],
        embeddings=[[0.0] * 768],
        documents=[json.dumps(data)],  # now stores embeddings too
        metadatas=[{"type": "suggestions", "page_num": 0, "chunk_id": -1}]
    )
    print(f"Saved {len(suggestions)} suggestions with embeddings for '{doc_id}'")

