def narrate_table(tables_list, page_num):
    print('narrate_table called for page: ',page_num,' Num of tables: ',len(tables_list))
    if not tables_list:
        return []

    all_chunks = []
    for table in tables_list:
        if not table["block"].strip():
            continue

        prompt = f"""You are a financial analyst. Convert this single financial table into clear sentences.
The table context/heading tells you what the table is about.
IMPORTANT: If the values are percentages, state them as percentages NOT dollar amounts.
If the values are dollar amounts, state them in millions of dollars.

Table context/heading: {table["context"]}

Table data:
{table["block"]}

Write only the sentences, nothing else."""

        r = requests.post(f"{OLLAMA_BASE}/api/generate", json={
            "model": LLM_MODEL,
            "prompt": prompt,
            "stream": False
        })

        narrative = r.json().get("response", "").strip()
        narrative = re.sub(r"<think>.*?</think>", "", narrative, flags=re.DOTALL).strip()

        if not narrative:
            continue

        sentences = re.split(r"(?<=[.!?])\s+", narrative)
        buffer = ""
        for sentence in sentences:
            if len((buffer + " " + sentence).split()) < 150:
                buffer += " " + sentence
            else:
                if buffer.strip():
                    all_chunks.append({
                        "text": f"{buffer.strip()}\n[Page {page_num}]",
                        "page_num": page_num,
                        "type": "table"
                    })
                buffer = sentence

        if buffer.strip():
            all_chunks.append({
                "text": f"{buffer.strip()}\n[Page {page_num}]",
                "page_num": page_num,
                "type": "table"
            })

    return all_chunks


