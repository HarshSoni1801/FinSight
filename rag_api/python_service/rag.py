def ask_llm_stream(question, context):
    prompt = f"""You are a helpful assistant that answers questions about company financial documents.
Answer the question using ONLY the context provided below.
If the answer is not found in the context, say "I could not find this in the document."

Context:
{context}

Question: {question}

Answer clearly and concisely."""

    r = requests.post(f"{OLLAMA_BASE}/api/generate", json={
        "model": LLM_MODEL,
        "prompt": prompt,
        "stream": True   # ← only difference from ask_llm
    }, stream=True)

    think_buffer = ""
    in_think = False

    for line in r.iter_lines():
        if not line:
            continue
        try:
            chunk = json.loads(line)
        except:
            continue

        token = chunk.get("response", "")

        # strip <think> tags from deepseek on the fly
        think_buffer += token
        while True:
            if in_think:
                end = think_buffer.find("</think>")
                if end != -1:
                    think_buffer = think_buffer[end + 8:]
                    in_think = False
                else:
                    think_buffer = ""
                    break
            else:
                start = think_buffer.find("<think>")
                if start != -1:
                    yield think_buffer[:start]
                    think_buffer = think_buffer[start + 7:]
                    in_think = True
                else:
                    yield think_buffer
                    think_buffer = ""
                    break

