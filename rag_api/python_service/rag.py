def chunk_text(pages, chunk_size=200, overlap=50,  on_page=None):
    print('chunk_text called. creating chunks. Num of pages: ',len(pages))
    chunks = []
    for page in pages:

        if on_page:
            on_page(page["page_num"], len(pages))

        paragraphs = re.split(r"\n\s*\n", page["text"])
        for para in paragraphs:
            words = para.split()
            if len(words) < 8:
                continue
            if para.lower().strip() in ["table of contents"]:
                continue
            if len(set(words)) < 3:
                continue

            start = 0
            while start < len(words):
                chunk_words = words[start:start + chunk_size]
                if len(chunk_words) < 20 and start > 0:
                    break
                chunks.append({
                    "text": " ".join(chunk_words),
                    "page_num": page["page_num"],
                    "type": "text"
                })
                start += chunk_size - overlap

        table_chunks = narrate_table(page.get("tables", []), page["page_num"])
        chunks.extend(table_chunks)
      
    print('Chunks Created: ',len(chunks))
    return chunks


