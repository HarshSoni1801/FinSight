    def keyword_score(text):
        words = re.findall(r"\w+", question.lower())
                # return sum(word in text.lower() for word in words if len(word) > 2)
        #pythonic way to write the below.
        count = 0

        for word in words:              # 1. The Loop
            if len(word) > 2:           # 2. The Filter
                if word in text.lower(): # 3. The Action
                    count += 1          # 4. The Container (sum)
        return count;

    results["keyword_score"] = results["text"].apply(keyword_score)
    if results["keyword_score"].max() > 0:
        results["keyword_score"] = results["keyword_score"] / results["keyword_score"].max()

    results["type_score"] = (results["type"] == "table") * 0.02
    results["final_score"] = (
        results["score"] +
        0.2 * results["keyword_score"] +
        results["type_score"]
    )
    return results.sort_values(by="final_score", ascending=False)


