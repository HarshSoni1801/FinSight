# ── WHY FAISS WAS USED INITIALLY ──────────────────────────────────────────
# FAISS (Facebook AI Similarity Search) was our first vector search solution.
# It's extremely fast at finding similar vectors using cosine similarity via
# IndexFlatIP (Inner Product on normalized vectors). We built a custom
# persistence layer around it — saving the DataFrame (chunks + embeddings)
# as a .joblib file and the index as a .faiss file, then loading both back
# into a RAM dictionary (store={}) on startup.
#
# THE PROBLEM: Everything lived in RAM. Every document's embeddings were
# loaded into memory at startup and stayed there permanently. This works fine
# for 3-4 small PDFs during development, but doesn't scale — 50 large financial
# reports would exhaust memory. We also had to maintain our own brittle
# save/load/delete logic with no metadata filtering or querying capabilities.
# ──────────────────────────────────────────────────────────────────────────


# ── WHY CHROMADB REPLACED FAISS ───────────────────────────────────────────
# ChromaDB is a purpose-built vector database. Unlike FAISS which is just a
# search library requiring us to build everything around it, ChromaDB handles
# persistence, metadata storage, and similarity search natively as one system.
#
# KEY IMPROVEMENTS OVER FAISS:
# 1. Disk-based — collections are saved automatically, nothing lives in RAM
#    permanently. Documents are loaded on demand, not all at startup.
# 2. Metadata filtering — each chunk stores page_num and type alongside its
#    embedding, enabling future filtering like "search only table chunks" or
#    "search only pages 30-40".
# 3. No custom save/load code — ChromaDB manages its own persistence. No more
#    joblib.dump, faiss.write_index, or the store={} dictionary.
# 4. Scales cleanly — adding 100 documents doesn't increase startup RAM usage.
# 5. Foundation for multi-document querying — ChromaDB can query across
#    multiple collections, which FAISS required significant custom code for.
#
# NOTE: FAISS code is kept below as a reference and fallback during transition.
# Once ChromaDB is confirmed stable, the FAISS functions and store={} dict
# in main.py can be removed entirely.
# ──────────────────────────────────────────────────────────────────────────


