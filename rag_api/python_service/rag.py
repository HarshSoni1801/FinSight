def extract_pdf(file_path):
    print('Extracting PDF:', file_path)
    pages = []
    with pdfplumber.open(file_path) as pdf:
        for i, page in enumerate(pdf.pages):
            tables = page.find_tables()

            # 1. Clean text (exclude table bounding boxes)
            non_table_page = page
            for table in tables:
                bbox = table.bbox
                non_table_page = non_table_page.filter(
                    lambda obj, bbox=bbox: not (
                        obj["x0"] >= bbox[0] and obj["x1"] <= bbox[2] and
                        obj["top"] >= bbox[1] and obj["bottom"] <= bbox[3]
                    )
                )
            text = ftfy.fix_text(non_table_page.extract_text() or "")

            # 2. Extract tables with context
            tables_list = []
            full_text = ftfy.fix_text(page.extract_text() or "")
            lines = full_text.split("\n")

            for table in tables:
                table_block = ""
                for row in table.extract():
                    clean_row = [cell or "" for cell in row]
                    row_text = " | ".join(clean_row).strip()
                    if "|" not in row_text:
                        continue
                    table_block += row_text + "\n"

                if not table_block.strip():
                    continue

                # Get 2 lines of context above table using bbox
                table_top = table.bbox[1]
                above_lines = []
                for obj in page.chars:
                    if obj["bottom"] < table_top:
                        above_lines.append((obj["bottom"], obj["text"]))

                above_lines.sort(key=lambda x: x[0])
                grouped = groupby(above_lines, key=lambda x: round(x[0], 1))
                text_lines = []
                for _, chars in grouped:
                    line_text = "".join(c[1] for c in chars).strip()
                    if line_text:
                        text_lines.append(line_text)

                context_lines = [l for l in text_lines if l.strip()][-6:]
                header_text = "\n".join(context_lines)

                tables_list.append({
                    "context": header_text,
                    "block": table_block,
                    "text_lines": text_lines
                })

            pages.append({
                "text": text,
                "tables": tables_list,
                "page_num": i + 1
            })

    return pages


